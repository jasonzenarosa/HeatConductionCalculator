def num_input(name):
    return float(input(name + ":"))


def heat():
    return num_input("Q")


def time():
    return num_input("t")


def thermal():
    return num_input("k")


def initial_temp():
    return num_input("T(initial)")


def final_temp():
    return num_input("T(final)")


def area():
    return num_input("A")


def length():
    return num_input("L")

